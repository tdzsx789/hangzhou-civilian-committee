  // 遍历 list 中的每个元素，如果 images 中的元素没有 url，则根据 name 从 assets 中匹配图片
  const processedList = useMemo(() => {
    return list.map(item => ({
      ...item,
      images: item.images.map(image => {
        // 如果 url 为空或未定义，则根据 name 匹配图片
        if (!image.url && image.name) {
          const matchedImage = getImageByName(image.name);
          if (matchedImage) {
            return {
              ...image,
              url: matchedImage
            };
          }
        }
        return image;
      })
    }));
  }, []); // eslint-disable-line react-hooks/exhaustive-deps

  const handleLearnMore = () => {
    setCurrentPage('detail1');
  };

